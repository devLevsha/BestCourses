package by.potato.Pairs;

public class WorkAdditionalInfo {
    private boolean work;
    private String info;

    public WorkAdditionalInfo(boolean work, String info) {
        this.work = work;
        this.info = info;
    }

    public boolean isWork() {
        return work;
    }

    public String getInfo() {
        return info;
    }
}
